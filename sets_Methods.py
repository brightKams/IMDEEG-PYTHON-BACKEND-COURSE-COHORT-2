#  1. add(): Adds an element tot the set

#  2. clear() Removes all the elements from the set

#  3. copy() Returns a copy of the set

#  4. difference() Returns a set containing the difference between two or more sets

#  5. difference_update() Removes the items in thos set that are also included in another, specified set

#  6. discard() Removes the specified item

#  7. intersection() Returns a set, that is the intersection of two other sets

#  8. intersection_update() Removes the items in this set that are not present in other, specified set(s)

#  9. isdisjoint() Returns whether another set contains this set or not

# 10. issubset() Returns whether another set contains this set or not

# 11. isuperset() Returns whether this set contains another set or not

# 12. pop() Removes an element from the set

# 13. remove() Removes the specified element 

# 14. symmetric_difference() Returns a set with the symmetric difference of two sets

# 15. symmetric_diifferece_update() inserts the symmetric differences from this set and another

